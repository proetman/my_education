# # EC2
# 

# * EC2 stands for Elastic Compute Cloud
# 
# * web service that provides resizable compute compute capacity in cloud.
# * reduce time req to obtain, boot new server to minutes
# * allows quick scale in capacity both up and down and requirements change.
# 
# * what is it? It is a virtual linux|windows server
# 
# * Startups now do not require dollars for hardware, they can experiment!
# 
# * allow you to pay only for the capacity that you actually use.
# * provides dev the tools to build failure resilient applications and isolate themselves from common failure scenarios
# 
# ## EC2 Options
# 
# * On Demand allow you to pay fixed rate by hour or second with no commitment
# * reserved provide you with capacity reservation, offer significant discount on hourly charge, 1 year or 3 year terms.
# * spot bid whatever price you want for instance capacity, providing for even greater savings if your applications have flexible start and end times. You set your bid price (how much you are willing pay), if the price goes above your price, your machine shuts down. If below, then your machine fires up!
# * Dedicated Hosts physical ec2 server dedicated for your use. reduce cost by allowing you to use your existing server-bound software licenses.
